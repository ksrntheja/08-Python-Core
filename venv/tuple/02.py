t = ()
print(t, type(t))

t = (10)
print(t, type(t))

t = (10,)
print(t, type(t))

t = 10,
print(t, type(t))

t = 10, 20,
print(t, type(t))

t = (10, 20,)
print(t, type(t))

# () <class 'tuple'>
# 10 <class 'int'>
# (10,) <class 'tuple'>
# (10,) <class 'tuple'>
# (10, 20) <class 'tuple'>
# (10, 20) <class 'tuple'>
