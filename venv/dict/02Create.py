d = {}
print(d, type(d))

print()

d = dict()
print(d, type(d))

d[100] = "theja"
d[200] = "ravi"
d[300] = "shiva"
print(d, type(d))

print()

d = {100: 'theja', 200: 'ram'}
print(d, type(d))

# {} <class 'dict'>
#
# {} <class 'dict'>
# {100: 'theja', 200: 'ravi', 300: 'shiva'} <class 'dict'>
#
# {100: 'theja', 200: 'ram'} <class 'dict'>
