print('~4    -> ', ~4)
print('~5    -> ', ~5)
print('~-4   -> ', ~-4)

print('~True   -> ', ~True)
print('~False  -> ', ~False)

print()

print('-True   -> ', -True)
print('-False  -> ', -False)

print()

print('~-True   -> ', ~-True)
print('~-False   > ', ~-False)

# ~4    ->  -5
# ~5    ->  -6
# ~-4   ->  3
# ~True   ->  -2
# ~False  ->  -1
#
# -True   ->  -1
# -False  ->  0
#
# ~-True   ->  0
# ~-False   >  -1
