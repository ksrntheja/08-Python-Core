a = 10.0
b = 2

print('a = ', a)
print('b = ', b)

print('a +  b = ', a + b)
print('a -  b = ', a - b)
print('a *  b = ', a * b)
print('a /  b = ', a / b)
print('a // b = ', a // b)
print('a %  b = ', a % b)
print('a ** b = ', a ** b)

