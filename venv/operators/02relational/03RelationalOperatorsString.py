a = 'a'  # 97
b = 'b'  # 98

print('a = ', a)
print('b = ', b)

print('ord("a") = ', ord("a"))
print('ord("b") = ', ord("b"))

print('chr(97)  = ', chr(97))
print('chr(98)  = ', chr(98))

print("a >  b   is ", a > b)
print("a >= b   is ", a >= b)
print("a <  b   is ", a < b)
print("a <= b   is ", a <= b)

# a =  a
# b =  b
# ord("a") =  97
# ord("b") =  98
# chr(97)  =  a
# chr(98)  =  b
# a >  b   is  False
# a >= b   is  False
# a <  b   is  True
# a <= b   is  True
