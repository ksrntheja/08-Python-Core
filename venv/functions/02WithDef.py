def calc(a, b):
    print('{} + {} = '.format(a, b), a + b)
    print('{} - {} = '.format(a, b), a - b)
    print('{} * {} = '.format(a, b), a * b)
    print()


calc(10, 20)
calc(100, 200)
calc(1000, 2000)

# 10 + 20 =  30
# 10 - 20 =  -10
# 10 * 20 =  200
#
# 100 + 200 =  300
# 100 - 200 =  -100
# 100 * 200 =  20000
#
# 1000 + 2000 =  3000
# 1000 - 2000 =  -1000
# 1000 * 2000 =  2000000
